//instantiate a contract object
const contract = new web3.eth.Contract(abi, contractAddress)

//run some methods in the contract using javascript

const getName = async() => {
	let name = await contract.methods.name().call()
	return `Name: ${name}`
}

const getTotalSupply = async() => {
    let totalSupply = await contract.methods.totalSupply().call()
    return `Total supply is: ${totalSupply}`
}

const getSymbol = async() => {
    let symbol = await contract.methods.symbol().call()
    return `Symbol is: ${symbol}`
}

const getBalanceOfOwner = async(owner) => {
	let ownerBalance = await contract.methods.balanceOf(owner).call()
	return `Balance of owner is ${ownerBalance}`
}

const getTokenDecimals = async() => {
	let tokenDecimals = await contract.methods.decimals().call()
	return `Token Decimal is ${tokenDecimals}`
}

